      subroutine Dmat_DFT(n,Dmat1,Dmat2,Dmat4,DFT)
      
      integer,                  intent(in   )   :: n
      real(wp), dimension(:,:) ,intent(  out)   :: Dmat1, Dmat2, Dmat4, DFT

      real(qp), dimension(:,:), allocatable     :: DiagQ, DFTQ, Dmat1Q, Dmat2Q, Dmat4Q

      real(qp)                                  :: theta

      integer                                   :: i, j, nb2, ii

        allocate(DiagQ(n,n),DFTQ(n,n),Dmat1Q(n,n),Dmat2Q(n,n),Dmat4Q(n,n))

        DiagQ(:,:) = 0.0_qp ; DFTQ(:,:) = 0.0_qp ;  Dmat1Q(:,:) = 0.0_qp ;  !  initialize matrices to zero

            if(mod(n,2) == 0) then                             !  n = even path

        elseif(mod(n,2) == 1) then                             !  n = odd  path

           nb2 = (n-1)/2 ;

           DFTQ(:,1) = 1.0_qp * sqrt(1.0_qp/n)  ;              !  constant scaled to unit length

           do i = 1,n
              theta = 2.0_qp * piq * (i-1) / n
              do j = 1,nb2
                 DFTQ(i,2*j+0) = sin(j*theta) * sqrt(2.0_qp/n) !  sine     scaled to unit length
                 DFTQ(i,2*j+1) = cos(j*theta) * sqrt(2.0_qp/n) !  cosine   scaled to unit length
              enddo
           enddo

           do i = 1,nb2
             ii = (i-1)*2 + 2
             DiagQ(ii+0,ii+1) = -i
             DiagQ(ii+1,ii+0) = +i
           enddo

           Dmat1Q = matmul(DFTQ,matmul(DiagQ,transpose(DFTQ))) !  matrix operations performed in quadruple precision
           Dmat2Q = matmul(Dmat1Q,Dmat1Q)                      !  matrix operations performed in quadruple precision
           Dmat4Q = matmul(Dmat2Q,Dmat2Q)                      !  matrix operations performed in quadruple precision

           Dmat1  = Dmat1Q ; Dmat2  = Dmat2Q ; Dmat4  = Dmat4Q ; DFT  = DFTQ ;!  truncate

        endif

        deallocate(DiagQ,DFTQ,Dmat1Q,Dmat2Q,Dmat4Q)
        
      end subroutine Dmat_DFT

